          public void SavingandRetrieving(){
              try{    
                    FileOutputStream outFile = new FileOutputStream(new File("AdminAndUser.ser"));
                            ObjectOutputStream outObj =   new ObjectOutputStream(outFile);
                             outObj.writeObject(arrayAdmin);                                           
                       outObj.close();
                }catch(IOException e) {
                    System.out.println(e);
                }
              
      }
  
        public void fillArrayList()  {
         try{
                   FileInputStream fileInputStream=new FileInputStream(new File("AdminAndUser.ser"));
                    ObjectInputStream stream=new ObjectInputStream(fileInputStream);
                    for(Object objectAdmin : (ArrayList)stream.readObject())
                    {
                       AdminAndUserModule module = (AdminAndUserModule) objectAdmin;
                        arrayAdmin.add(module);
                    }
               fileInputStream.close();
                    stream.close();
            }catch(IOException e){
                System.out.println("FileNotFound");   
            } catch (ClassNotFoundException ex) {
                System.out.println(ex);
        }
    }
          public void displayList(){
          arrayAdmin.clear();
           fillArrayList();
           DefaultTableModel model = (DefaultTableModel) tbl_admin.getModel();
            for(AdminAndUserModule admin: arrayAdmin){
                    if(admin.getUser().equals("admin")){
                        if(!admin.getId().equals(new Service().loginAdminOrUserID)){
                               model.addRow(new Object[]{admin.getUsername(), admin.getFirstname(), admin.getLastname()});
                        }
                         
                    }
           
            }
        
    }
