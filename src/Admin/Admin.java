    private void btn_viewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_viewActionPerformed
                 if(tbl_admin.getSelectionModel().isSelectionEmpty()){
                JOptionPane.showMessageDialog(null, "SELECT to View" , "View  VALIDATOR", WIDTH, null);
            }else{
                int index  = tbl_admin.getSelectedRow();
                TableModel model = tbl_admin.getModel();
                String username  = model.getValueAt(index, 0).toString();
                new Service().username = username;
                new View().setVisible(true);
        }
    }//GEN-LAST:event_btn_viewActionPerformed

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
             if(tbl_admin.getSelectionModel().isSelectionEmpty()){
                JOptionPane.showMessageDialog(null, "SELECT  TO DELETE" , "DELETE VALIDATOR", WIDTH, null);
            }else{
                int index  = tbl_admin.getSelectedRow();
                TableModel model = tbl_admin.getModel();
                String username  = model.getValueAt(index, 0).toString();
                
                int p = JOptionPane.showConfirmDialog(null, "Are you sure u want to delete this?" , "DELETE QUIZ" , JOptionPane.YES_NO_OPTION);
                
                    if(p == 0){
                           for(int i = 0; i < arrayAdmin.size() ; i++){
                               if(arrayAdmin.get(i).getUsername().contains(username)){
                                    arrayAdmin.remove(i);
                                   JOptionPane.showMessageDialog(null, "Successfully Deleted");
                                 
                               
                                   break;
                               }
                           }
                              SavingandRetrieving();
                              TableRemoveElement();
                              displayList();
                    }
                 
                    
            }
    }//GEN-LAST:event_btn_deleteActionPerformed

