    private void btn_search1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_search1MouseClicked
        
        if(txt_search.getText().isEmpty()){
         JOptionPane.showMessageDialog(null, "Seach Username Must not be Empty!!", "Search Validator", JOptionPane.WARNING_MESSAGE );
         return;
        }
        TableRemoveElement();
        DefaultTableModel model = (DefaultTableModel) tbl_admin.getModel();
        String searchVal  = txt_search.getText();
         for(AdminAndUserModule admin: arrayAdmin){
                    if(admin.getUsername().contains(searchVal)){
                          if(!admin.getId().equals(new Service().loginAdminOrUserID)){
                         model.addRow(new Object[]{admin.getUsername(), admin.getLastname(), admin.getFirstname()});
                          
                         }
                    }
               
            }
    }//GEN-LAST:event_btn_search1MouseClicked

