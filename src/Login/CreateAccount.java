        public void fillArrayList()  {
         try{
                   FileInputStream fileInputStream=new FileInputStream(new File("AdminAndUser.ser"));
                    ObjectInputStream stream=new ObjectInputStream(fileInputStream);
                    for(Object objectAdmin : (ArrayList)stream.readObject())
                    {
                       AdminAndUserModule module = (AdminAndUserModule) objectAdmin;
                        arrayAdmin.add(module);
                    }

            }catch(IOException e){
                System.out.println("FileNotFound");   
            } catch (ClassNotFoundException ex) {
                System.out.println(ex);
        }
    }
        public boolean isUsernameExist(String username){
            boolean isExist = false;
                for(int i = 0; i < arrayAdmin.size() ; i++){
                               if(arrayAdmin.get(i).getUsername().equals(username)){
                                   isExist = true;
                                   break;
                               }
                           }
                return isExist;
     }
          public String   QuizIdGenerator(String whereTo ){
         String id = "";
         Date now = new Date();
         int year = now.getYear();
         int date = now.getDate();
         int month = now.getMonth()+ 1;
             if(arrayAdmin.isEmpty()){
                id = whereTo + (year + 1900) + "-"+ month + "/"+date+ "-" + "1101";
        }else{
             int lastQuizIdIndex =  arrayAdmin.size() - 1;
            String adminValueID =  arrayAdmin.get(lastQuizIdIndex).getId();
            String [] arrayLastId = adminValueID.split("-", -1);
            id =    whereTo + (year + 1900) + "-"+ month+ "/"+date+ "-" + (Integer.parseInt(arrayLastId[2]) + 1);
        }
           
         return id;
     }

