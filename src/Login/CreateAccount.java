    private void btn_createActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_createActionPerformed
            
        if(!ck_admin.isSelected() && !ck_user.isSelected()){
                JOptionPane.showMessageDialog(null , "Please Select if you are a User or Admin", "Create Validator", JOptionPane.WARNING_MESSAGE);
                return;
        }
                        String username  = txt_username.getText();
                        String password = txt_password.getText();
                        String firstname  = txt_firstname.getText();
                        String lastname = txt_lastname.getText();
                        String ID = QuizIdGenerator(where);
                        
                if(username.isEmpty() || password.isEmpty() || firstname.isEmpty() || lastname.isEmpty() ){ 
                        JOptionPane.showMessageDialog(null, "Complete All the Details", "Saving Validator", WIDTH, null);
                        return;
                }
              
                if(isUsernameExist(username)){
                    JOptionPane.showMessageDialog(null, "Username Already Taken", "Saving Validator", WIDTH, null);
                        return;
                }
                try{
                        admin = new AdminAndUserModule(ID,username,new Service().md5_Password(password),firstname,lastname, where);
                        arrayAdmin.add(admin);
                        timer.start();
                        SavingandRetrieving();
                        new Service().loginUsername = username;
                         new Service().loginAdminOrUserID =  ID;
                         Correct  correct = new Correct();
                         correct.pack();
                          correct.lbl_exit.setVisible(false);
                           correct.jLabel2.setText("Successfully Created");
                           correct.setVisible(true);
                                    
                        this.dispose();
                }catch(Exception e){
                    JOptionPane.showMessageDialog(null, "e", "Saving Validator", WIDTH, null);
                }
    }//GEN-LAST:event_btn_createActionPerformed

